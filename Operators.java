public class Operators {
    public static void main(String[] args) {
        int num = 10;
        num += 5;
        System.out.println("After += operation: " + num);
        num -= 3;
        System.out.println("After -= operation: " + num);
        num *= 2;
        System.out.println("After *= operation: " + num);
    }
}
